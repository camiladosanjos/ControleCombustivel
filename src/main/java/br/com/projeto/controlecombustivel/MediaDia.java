package br.com.projeto.controlecombustivel;

public class MediaDia {

	private int dia;
	private double dMediaDia;

	
	public MediaDia(int dia, double dMediaDia) {
		
		this.dia = dia;
		
		this.dMediaDia = dMediaDia;
	}

	
	public int getDia() {
	
		return dia;
	
	}

	
	public double getdMediaDia() {
	
		return dMediaDia;
	
	}

}
